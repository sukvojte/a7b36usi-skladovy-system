    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    	
    	
    	if(tbPassword.getPassword().length > 0){
    		if(!Arrays.equals(tbPassword.getPassword(), tbPasswordAgain.getPassword())){
    			JOptionPane.showMessageDialog(null, "Hesla se neshodují");
    			return;
    		}
    	}
    	
    	if(tbName.getText().isEmpty()){
    		JOptionPane.showMessageDialog(null, "Jméno musí být vyplněné");
			return;
    	}
    	
    	UserRole acl = UserRole.values()[cbRole.getSelectedIndex()];
    	
    	
    	if(user != null){
	    	user.setUsername(tbName.getText());
	    	
	    	user.setAcl(acl);
	    	
	    	userController.updateUser(user);
	    	
	    	if(tbPassword.getPassword().length > 0){
	    		userController.updatePassword(user, tbPassword.getPassword());
	    	}
    	}else{
    		if(tbPassword.getPassword().length == 0){
    			JOptionPane.showMessageDialog(null, "Heslo musí být vyplněné");
    			return;
    		}
    		userController.createUser(tbName.getText(), tbPassword.getPassword(), acl);
    	}
    	setVisible(false);
    	
    }//GEN-LAST:event_btnSaveActionPerformed

