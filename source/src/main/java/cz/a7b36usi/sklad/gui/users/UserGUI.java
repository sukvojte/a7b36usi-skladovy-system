    public void setUser(UserDTO user) {
    	
    	this.user = user;
    	
    	roleModel = new ComboBoxModel<UserRole>() {
    		UserRole selected = null;
			public int getSize() {
				return UserRole.values().length;
			}

			public UserRole getElementAt(int index) {
				return UserRole.values()[index];
			}

			public void addListDataListener(ListDataListener l) {}

			public void removeListDataListener(ListDataListener l) {}

			public void setSelectedItem(Object anItem) {
				selected = (UserRole)anItem;
			}

			public UserRole getSelectedItem() {
				return selected;
			}
    	};
    	
    	cbRole.setModel(roleModel);
    	
    	if(user != null){
	    	tbName.setText(user.getUsername());
	    	
	    	int i = 0;
	    	for(UserRole r : UserRole.values()){
	    		if(r == user.getAcl()){
	    			cbRole.setSelectedIndex(i);
	    			break;
	    		}
	    		i++;
	    	}
    	}else{
    		tbName.setText("");
    		cbRole.setSelectedIndex(0);
    	}
    	
    }
